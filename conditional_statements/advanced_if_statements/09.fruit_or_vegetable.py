thing = input()

if thing == "banana" or thing == "apple" or  thing == "kiwi" or  thing == "cherry" or  thing == "lemon" or thing == "grapes" :
    print("fruit")

elif thing == "tomato" or thing == "cucumber" or  thing == "pepper" or  thing == "carrot":
    print("vegetable")

else:
    print("unknown")
